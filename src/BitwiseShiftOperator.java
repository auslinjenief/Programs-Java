
public class BitwiseShiftOperator {
	public static void main(String[] args) {
		int a=25,b=45;
		System.out.println(a&b);
		System.out.println(a|b);
		System.out.println(a^b);
		System.out.println(~a);
		
		//Bitwise and shift operator
//		Bitwise And - &
//		Bitwise or  - |
//		Bitwise Xor  - ^
//		Bitwise Not  - ~
		
		
		
	}

}
