
//addition
//subtraction
//Multiplication
//Division
//Modulus
public class operators {
	public static void main(String args[]) {
		int a = 10;
		int b = 25;
		int c = a+b;
		int d = a-b;
		int e = a*b;
		int f = a/b;
		int g = a%b;
		System.out.println("addition : "+c);
		System.out.println("subtraction : "+d);
		System.out.println("Multiplication : "+e);
		System.out.println("Division : "+f);
		System.out.println("Modulus : "+g);
		
	//=================================================================>	
		
		//Arithmetic assignment operator
   /*	
	                =   a  = 10   a =10
	               +=   a += 10   a =a+10	
	               -=   a -= 10   a =a-10
	               *=   a *= 10   a =a*10
	               /=   a /= 10   a =a/10
	               %=   a %= 10   a =a%10
  */
		int x=10;
		x+=20;
		System.out.println(x);
		
		
//=====================================================================>		
		
		//Relational operator
	/*	
		operator             Name               			Example
		  ==				Equal to						a == b
		  !=				Not equal						a != b
		  >					Greater than					a > b
		  <					Less than						a < b
		  >=				Greater than or equal to		a >= b
		  <=				Less than or equal to 			a <= b
	*/	  
		  
		  //Logical operator
	/*
	       and       &&
	       or		 ||	
	       not			
	 */
		  
		int m1 = 25 , m2 = 75;
		System.out.println("And operator"+(m1>=35 && m2>=35));
		System.out.println("Or operator"+(m1>=35 || m2>=35));
		
//	=================================================================>	
	
					
		  
		  
	}	  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		  
		
	}

